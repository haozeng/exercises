The case was an open-ended invitation to identify a problem 
with elevators and design a software-based solution. The 
interviewers were aggressive about asking why I chose the topic 
I did, what I wanted to do with it, etc. They wanted to make sure 
I could have strong opinions and back them up. I think the process 
was supposed to mimic a three-way brainstorming session, with me 
coming up with ideas and them trying to pick them apart. It all 
felt quite fair.